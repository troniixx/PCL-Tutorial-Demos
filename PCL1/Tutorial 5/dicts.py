# random dict
my_dict = {"a": 1, "b": 2, "c": 3, "d": 4}

print(my_dict)
print(type(my_dict))
print("\n")

my_keys = my_dict.keys()
print(my_keys)
print(type(my_keys))
print("\n")

my_values = my_dict.values()
print(my_values)
print(type(my_values))
print("\n")

my_length = len(my_dict)
print(my_length)
print(type(my_length))
print("\n")

my_pairs = my_dict.items()
print(my_pairs)
print(type(my_pairs))
